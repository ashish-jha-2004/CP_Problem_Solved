typedef long long ll;
#define fl(i, j) for(int i{0}; i<j; i++)
#define finl(i, j, k) for(int i{j}; i<k; i++)
#define no cout << "NO\n";
#define yes cout << "YES\n";

void solve(){
    // code here
    ll n, k, x;
    cin >> n >> k >> x;
    ll a = n-k;
    ll sn = (n*(n+1))/2;
    ll sa = (a*(a+1))/2;
    ll sk = (k*(k+1))/2;
    if(sn-sa >= x && sk <= x){
        yes
        return;
    }
    no
    return;
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(NULL);
    int t;
    cin >> t;
    while (t--){
        solve();
    }
    return 0;
}